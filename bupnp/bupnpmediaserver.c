int content_directory_action(upnp_server_t *server, upnp_service_t *service, const char *action)
{
    media_obj_t *pm;
    upnp_val_t *val;
    char objID[64];
    char browseFlag[32];
    int startIndex;
    int requestedCount;
    int numReturned;
    int result;
    int made;

    butil_log(4, "ContentDirectory Action: %s\n", action);
    //butil_log(3, (char*)server->soap.data);

    if (! strcmp(action, "Browse"))
    {
        // parse input arguments
        //
        result = upnp_get_arg_value_as_string(service, action, "ObjectID", objID, sizeof(objID), NULL, NULL);
        if (result)
        {
            butil_log(2, "Missing ObjectID in Browse\n");
            return -1;
        }

        result = upnp_get_arg_value_as_string(service, action, "BrowseFlag", browseFlag, sizeof(browseFlag), NULL, NULL);
        if (result)
        {
            butil_log(2, "Missing BrowseFlag in Browse\n");
            return -1;
        }

        result = upnp_get_arg_value_as_int(service, action, "StartingIndex", &startIndex);
        if (result)
        {
            butil_log(2, "Missing StartingIndex in Browse\n");
            return -1;
        }

        result = upnp_get_arg_value_as_int(service, action, "RequestedCount", &requestedCount);
        if (result)
        {
            butil_log(4, "Missing RequestedCount in Browse\n");
            requestedCount = 0xFFFFFFFF;
            result = 0;
        }

        butil_log(4, "\nBrowse %s ID:%s start=%u count=%u\n", browseFlag, objID, startIndex, requestedCount);

        iostream_reset_ring(&s_reply);

        made = snprintf(s_reply.data, s_reply.size, s_didl_header);
        s_reply.head += made;
        s_reply.count += made;

        // find object id in server media tree and return first level children
        //
        numReturned = 0;

        if (! strcmp(objID, "0"))
        {
            pm = s_media_tree->root;
        }
        else
        {
            pm = media_get_obj_by_id(s_media_tree, objID);

            if (pm)
            {
                if (strcmp(pm->id, objID))
                {
                    butil_log(1, "Get by ID failed, got %s instead of %s\n", pm->id, objID);
                }
            }
        }
        if (pm)
        {
            if (! strcmp(browseFlag, "BrowseMetadata"))
            {
                made = snprintf(
                                s_reply.data + s_reply.head,
                                s_reply.size - s_reply.count,
                                s_didl_folder_item,
                                pm->id,
                                pm->parent ? pm->parent->id : "-1",
                                (int)media_child_count(pm),
                                pm->title ? pm->title : "no-title"
                                );
                s_reply.head += made;
                s_reply.count += made;

                butil_log(4, "Add metadata node %s:%d:%s\n", pm->id, pm->kid_num, pm->title ? pm->title : "");

                numReturned++;
            }
            else if (! strcmp(browseFlag, "BrowseDirectChildren"))
            {
                media_obj_t *px;
                int numSkipped = 0;

                for (px = pm->child; px && (numSkipped < startIndex); px = px->sibling)
                {
                    numSkipped++;
                }

                for (pm = px; pm && numReturned < requestedCount; pm = pm->sibling)
                {
                    butil_log(4, "Add item node %s:%d:%s\n", pm->id, pm->kid_num, pm->title ? pm->title : "");

                    if (pm->type == mtFOLDER)
                    {
                        made = snprintf(
                                    s_reply.data + s_reply.head,
                                    s_reply.size - s_reply.count,
                                    s_didl_folder_item,
                                    pm->id,
                                    pm->parent ? pm->parent->id : "0",
                                    (int)media_child_count(pm),
                                    pm->title ? pm->title : "no-title"
                                    );
                    }
                    else
                    {
                        made = snprintf(
                                    s_reply.data + s_reply.head,
                                    s_reply.size - s_reply.count,
                                    s_didl_media_item,
                                    pm->id,
                                    pm->parent ? pm->parent->id : "0",
                                    pm->title ? pm->title : "no-title",
                                    pm->url ? pm->url : "http://www.hell.org"
                                    );
                    }
                    s_reply.head += made;
                    s_reply.count += made;

                    numReturned++;
                }
            }
            else
            {
                // return error envelope if this could ever happen
                butil_log(1, "No such flag %s\n", browseFlag);
            }
        }

        made = snprintf(s_reply.data + s_reply.head, s_reply.size - s_reply.count, s_didl_footer);
        s_reply.head += made;
        s_reply.count += made;

        // set output arguments
        //
        upnp_set_arg_value_from_string(service, action, "Result", s_reply.data);
        upnp_set_arg_value_from_int(service, action, "NumberReturned", numReturned);
        upnp_set_arg_value_from_int(service, action, "TotalMatches", numReturned);
        upnp_set_arg_value_from_int(service, action, "UpdateID", s_update_id);
    }
    return 0;
}

int build_sample_media_tree(upnp_server_t *server)
{
    media_tree_t *tree;
    media_obj_t *root;
    media_obj_t *topdir;
    media_obj_t *subdir;
    media_obj_t *child;

    tree = media_tree_create();
    if (! tree)
    {
        BERROR("Can't make tree");
        return -1;
    }

    s_media_tree = tree;

    root = media_create_in_tree("0", 0, mtFOLDER, "root", tree, NULL);
    tree->root = tree->current = root;

    topdir = media_create_in_tree("00A", 0, mtFOLDER, "Music", tree, root);

    child = media_create_in_tree("M1", 0, mtSONG, "asong1", tree, topdir);
    child = media_create_in_tree("M2", 1, mtSONG, "bsong2", tree, topdir);
    child = media_create_in_tree("M3", 2, mtSONG, "csong3", tree, topdir);

    // create top level dir videos
    topdir = media_create_in_tree("00B", 0, mtFOLDER, "Videos", tree, root);

    subdir = media_create_in_tree("0BA", 0, mtFOLDER, "Good", tree, topdir);

    child = media_create_in_tree("GV1", 0, mtVIDEO, "cvid1", tree, subdir);
    child = media_create_in_tree("GV2", 1, mtVIDEO, "bvid2", tree, subdir);
    child = media_create_in_tree("GV3", 2, mtVIDEO, "avid3", tree, subdir);

    subdir = media_create_in_tree("0BB", 1, mtFOLDER, "Bad", tree, topdir);

    child = media_create_in_tree("BV1", 0, mtVIDEO, "zvid1", tree, subdir);
    child = media_create_in_tree("BV2", 1, mtVIDEO, "yvid2", tree, subdir);
    child = media_create_in_tree("BV3", 2, mtVIDEO, "xvid3", tree, subdir);

    return 0;
}

int build_media_tree(upnp_server_t *server, const char *rootdir)
{
    media_tree_t *tree;
    media_obj_t *root;
    media_obj_t *subdir;
    media_obj_t *child;
    struct dirent *ent;
    char hostname[UPNP_MAX_URL];
    char location[UPNP_MAX_URL];
    bipv4addr_t myipv4addr;
    bipv6addr_t myipv6addr;
    int iface_index;

    char path[UPNP_MAX_URL];
    char idstring[64];

    struct {
        int  offset;
        DIR *dir;
        media_obj_t *tree;
        int itemno;
    }
    stack[UPNP_MAX_DIR_DEPTH];
    int  top;

    int  result;

    butil_log(2, "Generating media tree from %s\n", rootdir);

    result = butil_get_host_info(
                                hostname,
                                sizeof(hostname),
                                &myipv4addr,
                                &myipv6addr,
                                &iface_index
                                );
    if (result)
    {
        butil_log(1, "Can't get interface data\n");
        return result;
    }

    if (myipv4addr.addr != 0)
    {
        butil_str_for_ipv4(myipv4addr.addr, hostname, sizeof(hostname));
    }
    else
    {
        butil_str_for_ipv6(&myipv6addr, hostname, sizeof(hostname));
    }

    // make sure the dir has a slash
    //
    strncpy(path, rootdir, sizeof(path) - 1);
    path[sizeof(path) - 1] = '\0';

    // point to end of current dir path as place to appendd subdirs
    //
    top = 0;
    stack[top].offset = strlen(path);
    if (stack[top].offset >= (sizeof(path) - 2))
    {
        butil_log(1, "Start-path too long\n");
        return -1;
    }
    else
    {
        if (stack[top].offset > 0)
        {
            if (path[stack[top].offset - 1] != '/')
            {
                path[stack[top].offset] = '/';
                stack[top].offset++;
                path[stack[top].offset] = '\0';
            }
        }
    }

    tree = media_tree_create();
    if (! tree)
    {
        BERROR("Can't make tree");
        return -1;
    }

    stack[top].dir = opendir(path);
    if (! stack[top].dir)
    {
        butil_log(0, "Can't open directory %s\n", stack[top].offset);
        media_tree_destroy(tree);
        return -1;
    }

    root = media_create_in_tree("0", 0, mtFOLDER, "media", tree, NULL);
    tree->root = tree->current = root;

    if (! root)
    {
        butil_log(0, "Can't create root item\n");
        closedir(stack[top].dir);
        media_tree_destroy(tree);
        return -1;
    }

    stack[top].itemno = 0;
    stack[top].tree = root;
    s_media_tree = tree;

    // enumerate entries in the current (stack[top].dir) directory and
    // explore subdirs. I do this with my own stack to avoid recursion since
    // recursing with a max-path array in each call gets really large on the stack
    //
    do
    {
        ent = readdir(stack[top].dir);
        if (! ent)
        {
            // end of subdir, pop to previous level
            //
            closedir(stack[top].dir);
            top--;
            if (top >= 0)
            {
                butil_log(5, "End of dir %s\n", path + stack[top].offset);
                path[stack[top].offset] = '\0';
            }
            else
            {
                butil_log(5, "End of dir %s\n", path);
            }
        }
        else
        {
            if (ent->d_type == DT_DIR)
            {
                if (strcmp(ent->d_name, ".") && strcmp(ent->d_name, ".."))
                {
                    if (top >= UPNP_MAX_DIR_DEPTH - 1)
                    {
                        butil_log(1, "Max depth exceeded\n");
                    }
                    else
                    {
                        strncpy(path + stack[top].offset, ent->d_name, sizeof(path) - stack[top].offset - 1);
                        top++;
                        stack[top].offset = strlen(path);
                        if (stack[top].offset >= (sizeof(path) - 2))
                        {
                            butil_log(1, "Sub-path too long\n");
                            top--;
                        }
                        else
                        {
                            if (stack[top].offset > 0)
                            {
                                if (path[stack[top].offset - 1] != '/')
                                {
                                    path[stack[top].offset] = '/';
                                    stack[top].offset++;
                                    path[stack[top].offset] = '\0';
                                }
                            }

                            stack[top].itemno = 0;
                            stack[top].dir = opendir(path);

                            if (! stack[top].dir)
                            {
                                butil_log(0, "Can't open directory %s\n", path);
                                top--;
                            }
                            else
                            {
                                snprintf(idstring, sizeof(idstring), "%s-%x",
                                       stack[top - 1].tree->id, stack[top - 1].itemno/*, stack[top].itemno*/);
                                stack[top - 1].itemno++;
                                butil_log(4, "Add folder item %s at id %s\n", ent->d_name, idstring);

                                stack[top].tree = media_create_in_tree(idstring, 0, mtFOLDER, ent->d_name, tree, stack[top - 1].tree);
                                if (! stack[top].tree)
                                {
                                    butil_log(1, "Can't make item for folder %s\n", ent->d_name);
                                    top--;
                                }
                            }
                        }
                    }
                }
            }
            else
            {
                snprintf(idstring, sizeof(idstring), "%s-%x", stack[top].tree->id, stack[top].itemno);
                stack[top].itemno++;
                butil_log(4, "Add file item %s at id %s\n", ent->d_name, idstring);

                child = media_create_in_tree(idstring, 0, mtSONG, ent->d_name, tree, stack[top].tree);
                if (! child)
                {
                    butil_log(1, "Can't make item for %s\n", ent->d_name);
                }
                // form url of item
                //
                snprintf(path + stack[top].offset, sizeof(path) - stack[top].offset - 1, "%s", ent->d_name);
                result = butil_paste_url(
                                            location,
                                            sizeof(location),
                                            schemeHTTP,
                                            hostname,
                                            server->port,
                                            path + stack[0].offset /* dont include root of path */
                                        );
                path[stack[top].offset] = '\0';
                if (result)
                {
                    butil_log(1, "Can't paste url\n");
                }
                else
                {
                    butil_log(3, "Item url=%s\n", location);
                    media_set_url(child, location);
                }

                // Get meta-data of item  and set that TODO
                //
                media_set_title(child, ent->d_name);

           }
        }
    }
    while (top >= 0);

    return 0;
}

static void useage(const char *program, int l)
{
    butil_log(l, "Usage: %s -lpjh\n", program);
    butil_log(l, "  -l [level] - set the log level\n");
    butil_log(l, "  -p [port]  - set HTTP document server port\n");
    butil_log(l, "  -j         - don't add random jitter to SSDP\n");
    butil_log(l, "  -h         - help with usage\n");
}

int main(int argc, char **argv)
{
    upnp_server_t server;
    uint16_t port;
    const char *program, *arg;
    int loglevel = 3;
    int result;

#ifdef Windows
    WSADATA wsaData;

    result = WSAStartup(MAKEWORD(2,2), &wsaData);
    if (result != 0) {
        UPNP_ERROR("WSAStartup failed");
        return -1;
    }
#else
    signal(SIGPIPE, SIG_IGN);
#endif
    butil_set_log_level(loglevel);

#if UPNP_SUPPORT_TLS
    result = iostream_tls_prolog();
    if (result)
    {
        UPNP_ERROR("Can't init TLS");
        return -1;
    }
#endif

    program = *argv++;
    argc--;

    loglevel = 3;

    port = 8810;
    s_media_root_dir = NULL;

    result = 0;

    while (argc > 0 && ! result)
    {
        arg = *argv;
        if (arg[0] == '-')
        {
            switch (arg[1])
            {
            case 'p': case 'P':
                if ((*argv)[2])
                {
                    port = strtoul(*argv + 2, NULL, 0);
                }
                else if (argc > 1)
                {
                    argv++;
                    argc--;
                    port = strtoul(*argv, NULL, 0);
                }
                else
                {
                    useage(program, 0);
                    return -1;
                }
                break;
            case 'l': case 'L':
                if ((*argv)[2])
                {
                    loglevel = strtoul(*argv + 2, NULL, 0);
                }
                else if (argc > 1)
                {
                    argv++;
                    argc--;
                    loglevel = strtoul(*argv, NULL, 0);
                }
                else
                {
                    useage(program, 0);
                    return -1;
                }
                break;
            case 'j': case 'J':
                s_nojitter = true;
                break;
            case 'h': case 'H':
                useage(program, 1);
                return 0;
            case 'd': case 'D':
                if ((*argv)[2])
                {
                    s_media_root_dir = *argv + 2;
                }
                else if (argc > 1)
                {
                    argv++;
                    argc--;
                    s_media_root_dir = *argv;
                }
                else
                {
                    useage(program, 0);
                    return -1;
                }
                break;
            default:
                useage(program, 0);
                return -1;
            }
        }
        else if (! s_media_root_dir)
        {
            useage(program, 0);
            return -1;
        }
        argv++;
        argc--;
    }

    butil_set_log_level(loglevel);

    result = upnp_server_init(
                                &server,
                                port,
                                s_nojitter,
                                uuid_device,
                                "/device_description",
                                "MediaServer",
                                1,
                                2000
                            );
    if (result)
    {
        UPNP_ERROR("can't init server");
        return result;
    }

    if (! s_media_root_dir)
    {
        // generate a little media tree to server
        //
        result = build_sample_media_tree(&server);
    }
    else
    {
        result = build_media_tree(&server, s_media_root_dir);
    }

    s_reply.size = 32768;
    s_reply.data = (uint8_t*)malloc(s_reply.size);
    s_reply.head = 0;
    s_reply.tail = 0;
    s_reply.count = 0;

    result = upnp_add_text_url(&server, "/device_description", butil_mime_xml, s_device_description);
    if (result)
    {
        UPNP_ERROR("can't add description url");
        return result;
    }

    result = upnp_add_file_url(&server, "/icon128.png", butil_mime_png, s_icon128, s_icon128z);
    if (result)
    {
        UPNP_ERROR("can't add icon");
        return result;
    }

    if (s_media_root_dir)
    {
        result = upnp_add_directory_url(&server, s_media_root_dir);
        if (result)
        {
            UPNP_ERROR("can't add media directory");
            return result;
        }
    }

    // add a content directory service
    //
    result = upnp_add_service(
                                &server,
                                server.root_device,
                                content_directory_action,
                                "ContentDirectory",
                                1,
                                "/content_scpd",
                                s_content_directory_scpd,
                                "/content_control",
                                "/content_event"
                            );

    if (result)
    {
        UPNP_ERROR("can't add content service");
        return result;
    }

    // add a connection manager service
    //
    result = upnp_add_service(
                                &server,
                                server.root_device,
                                NULL, //on_conman,
                                "ConnectionManager",
                                1,
                                "/conman_scpd",
                                s_connection_manager_scpd,
                                "/conman_control",
                                "/conman_event"
                             );

     if (result)
     {
         UPNP_ERROR("can't add conman service");
         return result;
     }

     result = upnp_serve(&server, NULL, NULL);
     if (result)
     {
         butil_log(2, "server on port %u ends\n", server.port);
     }

     upnp_server_cleanup(&server);

     return result;
}

